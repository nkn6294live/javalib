package nkn6294.java.lib.json;

public class DataImportException extends Exception {
	public DataImportException() {
		super();
	}
	public DataImportException(String message) {
		super(message);
	}
	public DataImportException(Throwable throwable) {
		super(throwable);
	}
	public DataImportException(String message, Throwable throwable) {
		super(message, throwable);
	}
	public DataImportException(String message, Throwable throwable, boolean enableSuppression, boolean writableStackTrace) {
		super(message, throwable, enableSuppression, writableStackTrace);
	}
	private static final long serialVersionUID = -2563382022185697480L;
}
